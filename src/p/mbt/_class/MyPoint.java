package p.mbt._class;

public class MyPoint {
	
	public float x;
	public float y;
	
	public MyPoint(float X,float Y){
		x = X;
		y = Y;
	}
	
	public float getX(){
		return x;
	}	
	public float getY(){
		return y;
	}
	
	public void setX(float X){
		x = X;
	}
	public void setY(float Y){
		y = Y;
	}

}
